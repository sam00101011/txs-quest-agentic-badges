import { mkdir, readFile, writeFile } from "node:fs/promises";
import { dirname, resolve } from "node:path";
import { fileURLToPath } from "node:url";

const scriptDir = dirname(fileURLToPath(import.meta.url));
const projectRoot = resolve(scriptDir, "..");
const inputPath = resolve(projectRoot, "docs/current-badges.md");
const outputPath = resolve(projectRoot, "web/generated/badgeMarkdownCatalog.js");

function matchLine(section, pattern) {
  return section.match(pattern)?.[1]?.trim?.() ?? "";
}

function parseFiles(section) {
  return [...section.matchAll(/\`(pin\d+\.(?:jpg|mp4))\`/g)].map((match) => match[1]);
}

function buildDescription(badgeType, claimCondition) {
  const parts = [];
  if (badgeType) {
    parts.push(`${badgeType} badge.`);
  }
  if (claimCondition) {
    parts.push(claimCondition);
  }
  return parts.join(" ").trim();
}

function parseBadgeCatalog(markdown) {
  return markdown
    .split(/^###\s+/m)
    .slice(1)
    .map((section) => {
      const headingLine = section.split("\n", 1)[0]?.trim?.() ?? "";
      const headingMatch = headingLine.match(/^(\d+)\.\s+(.+)$/);
      if (!headingMatch) {
        return null;
      }

      const index = Number(headingMatch[1]);
      const name = headingMatch[2].trim();
      const slug = matchLine(section, /^- Slug:\s*\`([^\`]+)\`/m);
      const files = parseFiles(section);
      const badgeType = matchLine(section, /^- Badge type:\s*(.+)$/m);
      const claimCondition = matchLine(section, /^- Criteria:\s*(.+)$/m);
      const pinId = files[0]?.match(/(pin\d+)/)?.[1] ?? "";

      return {
        index,
        name,
        slug,
        pinId,
        files,
        badgeType,
        description: buildDescription(badgeType, claimCondition),
        claimCondition
      };
    })
    .filter(Boolean)
    .sort((first, second) => first.index - second.index);
}

function buildModule(entries) {
  const json = JSON.stringify(entries, null, 2);
  return `// Generated from docs/current-badges.md by scripts/generate-badge-markdown-catalog.mjs
// Do not edit this file manually.

export const BADGE_MARKDOWN_CATALOG = Object.freeze(${json});

export const BADGE_MARKDOWN_BY_SLUG = Object.freeze(
  Object.fromEntries(
    BADGE_MARKDOWN_CATALOG.filter((entry) => entry.slug).map((entry) => [
      entry.slug.toLowerCase(),
      entry
    ])
  )
);

export const BADGE_MARKDOWN_BY_PIN_ID = Object.freeze(
  Object.fromEntries(
    BADGE_MARKDOWN_CATALOG.filter((entry) => entry.pinId).map((entry) => [
      entry.pinId.toLowerCase(),
      entry
    ])
  )
);

export function getBadgeMarkdownBySlug(slug = "") {
  return BADGE_MARKDOWN_BY_SLUG[String(slug).trim().toLowerCase()] ?? null;
}

export function getBadgeMarkdownByPinId(pinId = "") {
  return BADGE_MARKDOWN_BY_PIN_ID[String(pinId).trim().toLowerCase()] ?? null;
}
`;
}

const markdown = await readFile(inputPath, "utf8");
const entries = parseBadgeCatalog(markdown);
await mkdir(dirname(outputPath), { recursive: true });
await writeFile(outputPath, buildModule(entries));
console.log(`Generated ${entries.length} badge metadata entries from docs/current-badges.md`);
